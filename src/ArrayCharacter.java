public class ArrayCharacter {

    public static void main(String[] args) {

        char[] chAr = new char[5];

        chAr[0] ='s';
        chAr[1] ='d';
        chAr[2] ='f';
        chAr[3] ='#';
        chAr[4] ='!';

        for(int i=0;i<chAr.length;i++)
            System.out.println(chAr[i]);


    }
}
